var width = parseInt(d3.select("#scatter")
  .style("width"));

// graph height
var height = width - width / 3.9;

// graph margin
var margin = 20;

// label area
var labelArea = 110;

// text padding 
var tPadBot = 40;
var tPadLeft = 40;

// generate canvas for graph
var svg = d3
  .select("#scatter")
  .append("svg")
  .attr("width", width)
  .attr("height", height)
  .attr("class", "chart");

// circle radius setup.

var circRadius;
function crGet() {
  if (width <= 530) {
    circRadius = 5;
  }
  else {
    circRadius = 10;
  }
}
crGet();

// bottom access labels
svg.append("g").attr("class", "xText");
var xText = d3.select(".xText");

// place the text for the xaxis at the bottom of the chart.

function xTextRefresh() {
  xText.attr(
    "transform",
    "translate(" +
      ((width - labelArea) / 2 + labelArea) +
      ", " +
      (height - margin - tPadBot) +
      ")"
  );
}
xTextRefresh();

